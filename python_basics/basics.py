# Python Basics - Getting Started

# This file contains basic Python concepts and examples

# 1. Variables and Data Types
name = "Python Learner"
age = 25
height = 5.9
is_student = True

print("Hello,", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

# 2. Basic Operations
a = 10
b = 3
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Integer Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

# 3. Strings
message = "Welcome to Python!"
print(message.upper())
print(message.lower())
print(len(message))
print(message[0:7])

# 4. Lists
fruits = ["apple", "banana", "orange"]
print("Fruits:", fruits)
fruits.append("grape")
print("After append:", fruits)
print("First fruit:", fruits[0])

